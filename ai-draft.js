// buildIOT() из gen_p2.js. Отличается только тело — оно от ИИ — и
// обязательная пометка на первой странице (см. DRAFT_DISCLAIMER).

const base = require('./gen_p1');
const { safe } = require('./utils');
const { declRole, p, bul, SH } = base;
const gen_p2 = require('./gen_p2');

const DRAFT_DISCLAIMER =
  'ПРОЕКТ ИНСТРУКЦИИ. Черновик сформирован автоматически и ТРЕБУЕТ ' +
  'ОБЯЗАТЕЛЬНОЙ ПРОВЕРКИ специалистом по охране труда перед введением в ' +
  'действие. Не является готовым к использованию документом.';

const SECTION_TITLES = [
  '1. Общие требования охраны труда',
  '2. Требования охраны труда перед началом работы',
  '3. Требования охраны труда во время работы',
  '4. Требования охраны труда в аварийных ситуациях',
  '5. Требования охраны труда по окончании работы',
];

function buildPrompt(position, industry) {
  const system =
    'Ты — эксперт по охране труда в РФ. Пишешь текст инструкции по охране ' +
    'труда для конкретной должности. Отвечай СТРОГО в формате JSON без ' +
    'markdown-разметки, без обратных кавычек, без пояснений вне JSON. ' +
    'Формат: {"sections":[{"title":"1. Общие требования охраны труда",' +
    '"paragraphs":["текст пункта 1.1...","текст пункта 1.2..."]},...]}. ' +
    'Разделов должно быть ровно 5, в указанном порядке, соответствующем ' +
    'структуре типовой инструкции по ОТ. Каждый пункт — отдельная строка ' +
    'массива paragraphs, начинающаяся с номера пункта (например, "3.1."). ' +
    'Пиши по существу, без общих фраз, ориентируясь на реальные риски ' +
    'именно этой профессии. Не выдумывай номера конкретных приказов ' +
    'Минтруда, если не уверен в их точности — используй общие формулировки ' +
    '("в соответствии с требованиями охраны труда РФ") вместо неточной ссылки.';

  const prompt =
    'Составь текст инструкции по охране труда для должности «' + position + '»' +
    (industry ? ' в организации сферы деятельности «' + industry + '»' : '') +
    '. Учти специфику именно этой профессии — реальные вредные и опасные ' +
    'факторы, необходимые СИЗ, характерные аварийные ситуации.';

  return { system, prompt };
}

function parseAiResponse(text) {
  // Модель иногда всё равно оборачивает JSON в ```json ... ``` несмотря на
  // инструкцию — подчищаем перед парсингом.
  const cleaned = text.replace(/```json|```/g, '').trim();
  const data = JSON.parse(cleaned);
  if (!data.sections || !Array.isArray(data.sections)) {
    throw new Error('Получен ответ не в ожидаемом формате (нет sections[])');
  }
  return data.sections;
}

// Собирает секции документа (SH + p) из ответа ИИ — тем же визуальным
// языком, что и обычные инструкции (H/SH/p/bul из gen_p1.js).
function sectionsToDoc(aiSections) {
  const out = [];
  // Пробегаем по ожидаемым 5 разделам, подставляя то, что реально прислал
  // ИИ — если раздела не хватает, пропускаем без падения (лучше неполный
  // черновик, который проверяющий дополнит, чем ошибка генерации).
  SECTION_TITLES.forEach((title, i) => {
    const found = aiSections[i];
    out.push(SH(title));
    const paragraphs = (found && Array.isArray(found.paragraphs)) ? found.paragraphs : [];
    if (!paragraphs.length) {
      out.push(p('[Раздел не сформирован автоматически — требует ручного заполнения]', { indent: true, bold: true }));
    } else {
      paragraphs.forEach(text => out.push(p(safe(text), { indent: true })));
    }
  });
  return out;
}

// Основная функция — вызывается из main.js IPC-обработчика (там же лимит
// вызовов на клиента/месяц, см. ai:draftInstruction).
// callAI — функция вызова ИИ из main.js (передаётся как зависимость, чтобы
// этот модуль не тянул на себя https/AI_PROXY_URL — держим его чисто про
// сборку документа, а не про транспорт).
async function draftInstruction(c, dir, position, callAI, industry) {
  const { system, prompt } = buildPrompt(position, industry);
  const aiResult = await callAI(prompt, system, 2000); // больше max_tokens, чем обычный callAI-дефолт — нужен полный текст 5 разделов
  if (!aiResult.ok) {
    throw new Error('Ошибка формирования черновика: ' + (aiResult.error || 'неизвестная ошибка'));
  }

  let aiSections;
  try {
    aiSections = parseAiResponse(aiResult.text);
  } catch (e) {
    throw new Error('Не удалось разобрать полученный ответ: ' + e.message);
  }

  const positionGen = declRole(position, 'gen');
  const disclaimerBlock = [
    p(DRAFT_DISCLAIMER, { bold: true, indent: false }),
    ...base.eL(1),
  ];
  const sections = [...disclaimerBlock, ...sectionsToDoc(aiSections)];

  const filename = 'ПРОЕКТ Инструкция по охране труда для ' + position + ' (черновик).docx';
  return gen_p2.buildIOT(c, '(ПРОЕКТ)', 'для ' + positionGen, sections, dir, filename);
}

module.exports = { draftInstruction, DRAFT_DISCLAIMER };
