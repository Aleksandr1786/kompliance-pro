// Простое числительное прописью для небольших целых чисел (используется в
// договорах: "5 (пять) человек", "550 (пятьсот пятьдесят) рублей"). Не
// претендует на полноту — покрывает диапазон, реалистичный для цены услуги
// и количества сотрудников малого/среднего клиента.
function numToWordsSimple(n){
  n=Math.round(Number(n)||0);
  if(n===0) return 'ноль';
  const ones=['','один','два','три','четыре','пять','шесть','семь','восемь','девять'];
  const teens=['десять','одиннадцать','двенадцать','тринадцать','четырнадцать','пятнадцать','шестнадцать','семнадцать','восемнадцать','девятнадцать'];
  const tens=['','','двадцать','тридцать','сорок','пятьдесят','шестьдесят','семьдесят','восемьдесят','девяносто'];
  const hundreds=['','сто','двести','триста','четыреста','пятьсот','шестьсот','семьсот','восемьсот','девятьсот'];
  function belowThousand(n){
    let parts=[];
    if(n>=100){ parts.push(hundreds[Math.floor(n/100)]); n%=100; }
    if(n>=10 && n<20){ parts.push(teens[n-10]); n=0; }
    else{
      if(n>=20){ parts.push(tens[Math.floor(n/10)]); n%=10; }
      if(n>=1) parts.push(ones[n]);
    }
    return parts.join(' ');
  }
  if(n<1000) return belowThousand(n);
  const thousands=Math.floor(n/1000), rest=n%1000;
  let thWord;
  const t10=thousands%10, t100=thousands%100;
  if(t100>=11&&t100<=14) thWord='тысяч';
  else if(t10===1) thWord='тысяча';
  else if(t10>=2&&t10<=4) thWord='тысячи';
  else thWord='тысяч';
  return (belowThousand(thousands)+' '+thWord+(rest?' '+belowThousand(rest):'')).trim();
}

