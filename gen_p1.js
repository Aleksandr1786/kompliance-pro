  // не оказывались сотрудники других должностей. Для документов,
  // относящихся ко всей организации (положения, программы, журналы),
  // positionFilter не передаётся — список остаётся полным.
  let emp=c.employees||[];
  if(positionFilter){
    const key=String(positionFilter).trim().toLowerCase();
    emp=emp.filter(e=>String(e.position||'').trim().toLowerCase()===key);
  }
  const colW=[500,2500,2500,1200,1372];
  const hdr=row([cell('№ п/п',colW[0],{bold:true,center:true}),cell('Фамилия И.О.',colW[1],{bold:true,center:true}),cell('Должность',colW[2],{bold:true,center:true}),cell('Дата',colW[3],{bold:true,center:true}),cell('Подпись',colW[4],{bold:true,center:true})]);
  const dr=emp.map((e,i)=>row([cell(String(i+1),colW[0],{center:true,sz:SZ_S}),cell(safe(e.full_name),colW[1],{sz:SZ_S}),cell(e.position,colW[2],{sz:SZ_S}),cell(safe(c.doc_date),colW[3],{center:true,sz:SZ_S}),cell('',colW[4],{sz:SZ_S})]));
