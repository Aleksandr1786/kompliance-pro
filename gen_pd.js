// Нормализация ПДн-полей клиента
function normPd(c) {
  c.pd_resp_name     = c.pd_responsible_name     || c.manager_name;
  c.pd_resp_pos      = c.pd_responsible_position || c.manager_position;
  c.pd_ispdn         = Array.isArray(c.pd_ispdn_list) && c.pd_ispdn_list.length
    ? c.pd_ispdn_list.map(i => i.name || i).join(', ')
    : '1С:Бухгалтерия';
  c.pd_ispdn_arr     = Array.isArray(c.pd_ispdn_list) && c.pd_ispdn_list.length
    ? c.pd_ispdn_list.map(i => i.name || i)
    : ['1С:Бухгалтерия'];
  // Форма для документов
  c.isIP             = c.form === 'ИП';
  c.isOOO            = c.form === 'ООО' || !c.form;
  c.isAO             = c.form === 'АО / ЗАО';
  // ОКВЭД — гостиница
  c.isHotel          = String(c.okved||'').startsWith('55');
  // Порядковый номер приказа ПДн (продолжаем с 20 от order_prefix ОТ)
  c.pd_order_base    = (parseInt(c.order_prefix)||3) + 20;
  return c;
}

function pdOrderNum(c, offset) {
  return String(c.pd_order_base + offset).padStart(2, '0');
}

// Блок подписи ответственного за ПД
