// ── ГЛАВНАЯ ФУНКЦИЯ ГЕНЕРАЦИИ ПДн ───────────────────────

async function generatePdPackage(client, settings, outputDir, tmpRoot) {
  const c = normPd(norm(Object.assign({}, client)));
  const s = settings || {};
  const path = require('path');
  const fs = require('fs');

  const dirs = {
    d1: path.join(outputDir, 'Персональные данные', 'Раздел 1. Приказы'),
    d2: path.join(outputDir, 'Персональные данные', 'Раздел 2. Политика и положение'),
    d3: path.join(outputDir, 'Персональные данные', 'Раздел 3. Согласия и обязательства'),
    d4: path.join(outputDir, 'Персональные данные', 'Раздел 4. Журналы'),
    d5: path.join(outputDir, 'Персональные данные', 'Раздел 5. Акты'),
    d6: path.join(outputDir, 'Персональные данные', 'Раздел 6. Планы'),
    d7: path.join(outputDir, 'Персональные данные', 'Раздел 7. Инструкции'),
    d8: path.join(outputDir, 'Персональные данные', 'Раздел 8. Обучение'),
  };

  // Создаём папки — НЕ удаляем файлы
  for (const d of Object.values(dirs)) fs.mkdirSync(d, {recursive:true});

  // Временная папка (берём из generator.js или создаём свою)
  const myTmpRoot = tmpRoot || path.join(outputDir, '__tmp_pd');
  fs.mkdirSync(myTmpRoot, {recursive:true});

  const generated = [], errors = [];
  const report = { userModified: [] };

  const { shouldOverwrite: _so } = (() => {
    try { return require('./generator'); } catch(e) { return {}; }
  })();

  // Локальная копия функции если generator не экспортирует её
  function _shouldOverwrite(basename, diskHashMap, currentClientHash) {
    const info = diskHashMap && diskHashMap[basename];
    if (!info) return true;
    if (info.diskHash === info.storedFileHash) return true;
    if (info.storedClientHash !== currentClientHash) return true;
    return false;
  }

  const run = async (fn, finalDir) => {
    const relPath = path.relative(outputDir, finalDir);
    const tmpDir = relPath ? path.join(myTmpRoot, relPath) : myTmpRoot;
    fs.mkdirSync(tmpDir, {recursive:true});

    try {
      const result = await fn(c, s, tmpDir);
      const files = Array.isArray(result) ? result : [result];

      for (const tmpFile of files) {
        const basename = path.basename(tmpFile);
        const finalFile = path.join(finalDir, basename);

        if (_shouldOverwrite(basename, s.diskHashMap, s.currentClientHash)) {
          fs.copyFileSync(tmpFile, finalFile);
          generated.push(finalFile);
        } else {
          generated.push(finalFile);
          report.userModified.push(basename);
        }
        try { fs.unlinkSync(tmpFile); } catch(e) {}
      }
    } catch(e) {
      errors.push(fn.name + ': ' + e.message);
    }
  };

  const hasEmployees = (c.employees || []).length > 0;
  const isOOO_or_AO  = c.isOOO || c.isAO;

  // ── БАЗОВЫЙ ПАКЕТ ──────────────────────────────────────
  await run(pd_rkn_memo, dirs.d1);     // Памятка РКН — всегда первой
  await run(pd_01_01, dirs.d1);
  await run(pd_01_02, dirs.d1);
  await run(pd_01_04, dirs.d1);
  await run(pd_02_01, dirs.d2);
  await run(pd_04_01, dirs.d4);
  await run(pd_04_02, dirs.d4);
  await run(pd_05_01, dirs.d5);
  await run(pd_05_02, dirs.d5);
  await run(pd_05_03, dirs.d5);
  await run(pd_06_01, dirs.d6);
  await run(pd_07_01, dirs.d7);
  await run(pd_07_02, dirs.d7);

  // ── ЕСЛИ ЕСТЬ СОТРУДНИКИ ────────────────────────────────
  if (hasEmployees) {
    await run(pd_01_03, dirs.d1);
    await run(pd_01_06, dirs.d1);
    await run(pd_01_08, dirs.d1);
    await run(pd_03_01, dirs.d3);
    await run(pd_03_02, dirs.d3);
    await run(pd_06_02, dirs.d6);
    await run(pd_08_01, dirs.d8);
  }

  // ── ООО/АО ─────────────────────────────────────────────
  if (isOOO_or_AO) {
    await run(pd_01_05, dirs.d1);
    await run(pd_02_02, dirs.d2);
    await run(pd_07_03, dirs.d7);
    await run(pd_07_04, dirs.d7);
    await run(pd_07_05, dirs.d7);
    await run(pd_07_06, dirs.d7);
  }

  // ── СРЕДНЕЕ/КРУПНОЕ ─────────────────────────────────────
  if (c.small || c.medium) {
    await run(pd_01_07, dirs.d1);
  }

  // ── ГОСТИНИЦЫ И РАЗМЕЩЕНИЕ ──────────────────────────────
  if (c.isHotel) {
    await run(pd_hotel_01, dirs.d8);
  }

  // Очищаем свою tmpRoot если создавали сами
  if (!tmpRoot) {
    try { fs.rmSync(myTmpRoot, {recursive:true, force:true}); } catch(e) {}
  }

  return { generated, errors, report };
}

