async function gen_05_employees(c,s,dir){
  const generated=[];
  const seen=new Set();
  let n=2;
  const instrPosKey=safe(c.instr_position).toLowerCase();
  for(let i=0;i<c.employees.length;i++){
    const emp=c.employees[i];
    const pos=(emp.position||'').trim();
    if(!pos) continue;                       // нет должности — пропускаем
    const key=pos.toLowerCase();
    if(seen.has(key)) continue;              // одна инструкция на должность
    seen.add(key);
    const num=String(n++).padStart(2,'0');

    // Сначала проверяем неофисные специализированные профессии — у них
    // полностью своё содержание инструкции, не сводится к общему шаблону.
    const specialized=SPECIALIZED_ROLES[key];
    if(specialized){
      const fname=await buildIOT(c,'№ '+num+'-ИОТ','для '+declRole(pos,'gen'),specialized(),dir,
        'Инструкция по охране труда для '+declRole(pos,'gen').replace(/[\/\\:*?"<>|]/g,'_')+'.docx',{agreedByOT:true,positionFilter:pos});
      generated.push(fname);
      continue;
    }

    // Иначе — общий офисный шаблон (buildOfficeInstruction), с учётом:
    // является ли сотрудник делегатом по инструктажам (совпадает с
    // c.instr_position), освобождён ли от инструктажа на рабочем месте
    // (флаг на самом сотруднике — prog_b_exempt, то же поле, что и в
    // gen_instructage.js для освобождения от программы Б), и есть ли у
    // должности известная разъездная специфика.
    const isDelegate = c.instrIsDelegate && key===instrPosKey;
    const isExempt = !!emp.prog_b_exempt && !isDelegate;
    const hazardOpts = OFFICE_ROLE_HAZARDS[key] || {};
    const fname=await buildOfficeInstruction(c,dir,{
      num, position:pos, isDelegate, isExempt, ...hazardOpts,
      filename:'Инструкция по охране труда для '+declRole(pos,'gen').replace(/[\/\\:*?"<>|]/g,'_')+'.docx',
    });
    generated.push(fname);
  }
  return generated;
}

