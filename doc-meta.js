// КомплаенсПро doc-meta.js — версии шаблонов документов (доказательная база)
//
// Зачем: фича «почему изменился документ» должна различать две причины
// изменения содержимого .docx:
//   1) изменилось ЗАКОНОДАТЕЛЬСТВО → мы поправили текст документа;
//   2) изменились ДАННЫЕ КЛИЕНТА (директор, ответственный, адрес и т.д.).
//
// Разделитель — version. Когда правишь текст конкретного документа из-за
// нового/изменённого НПА — ПОДНИМИ version для этого документа и обнови
// npa. Приложение сравнит version с тем, что было сохранено при прошлом
// формировании, и если version выросла — покажет клиенту причину
// «обновлены требования законодательства: <npa>».
//
// Документы, которых здесь нет, считаются version: 1 — то есть с момента
// внедрения фичи требования по ним не менялись. Добавлять запись нужно
// ТОЛЬКО когда реально меняешь текст документа из-за НПА.
//
// npa — человекочитаемое описание для отчёта клиенту, например:
// 'Приказ Минтруда №772н от 29.10.2021'.
//
// ПРИМЕР (когда понадобится поднять версию):
// 'Приказ о назначении ответственного за ПД.docx': {
//   version: 2,
//   npa: 'ПП РФ №1119 от 01.11.2012 (ред. от 01.03.2027)',
// },

