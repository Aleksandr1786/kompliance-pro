// Инструкция охраннику поста — формируется отдельным документом на каждый
// вид поста, который реально назначен хотя бы одному сотруднику (по
// employee.chop.post_type), а не на все виды сразу — чтобы не плодить
// неиспользуемые документы, как и с ИОТ по должностям (см. gen_01_05).
// Цикл — внутри функции (как gen_05_employees), run() вызывается один раз.
async function gen_chop_instructions(c, s, dir) {
  const usedPostTypes = new Set(
    (c.employees || []).filter(e => e.chop && e.chop.post_type).map(e => e.chop.post_type)
  );

  const results = [];
  for (const postType of usedPostTypes) {
    const label = CHOP_POST_TYPES[postType] || postType;
    const ch = [
      approvalOrder(c, oNum(c, 20)),
      ...eL(1),
      H('ИНСТРУКЦИЯ ОХРАННИКУ'),
      H('ПОСТА «' + label.toUpperCase() + '»'),
      H(safe(c.name).toUpperCase(), 22),
      ...eL(1),
      ...postInstructionBody(postType, c),
      ...devSign(c),
      ...famSheetOrder(c, 'пост «' + label + '»'),
    ];
    const result = await save(
      [{ properties: { page: { size: { width: 11906, height: 16838 }, margin: MP } }, footers: { default: footer('chop.0' + Object.keys(CHOP_POST_TYPES).indexOf(postType)) }, children: ch }],
      dir, 'Инструкция охраннику поста (' + label + ').docx'
    );
    results.push(result);
  }
  return results;
}

// Табель постов охраны — ключевой документ ЧОП: перечень объектов охраны
// (подразделений клиента) с указанием постов, режима несения службы и
// закреплённых сотрудников. Формируется ОДИН раз на клиента (не по постам).
async function gen_chop_post_table(c, s, dir) {
  const divisions = c.divisions || [];
  const employees = c.employees || [];
  const num = oNum(c, 21);

  // Группируем сотрудников с заполненными ЧОП-данными по объекту (подразделению).
  // Сотрудники без division_id попадают в общий блок "Без привязки к объекту" —
  // это сигнал незаполненных данных, а не ошибка генератора.
  const byDivision = new Map();
  employees.forEach(e => {
    if (!e.chop || !e.chop.post_type) return; // нет данных ЧОП — не участвует в табеле
    const key = e.division_id || null;
    if (!byDivision.has(key)) byDivision.set(key, []);
    byDivision.get(key).push(e);
  });

  const blocks = [];
  if (byDivision.size === 0) {
    blocks.push(p('Данные о постах не заполнены — заполните разряд/тип поста/режим смен в карточках сотрудников (иконка ЧОП).', { indent: true }));
  }
  for (const [divisionId, emps] of byDivision.entries()) {
    const division = divisions.find(d => d.id === divisionId);
    const objectName = division ? division.name : 'Объект не указан';
    blocks.push(SH('Объект: ' + objectName));
    emps.forEach(e => {
      const post = CHOP_POST_TYPES[e.chop.post_type] || e.chop.post_type || '—';
      const razryad = e.chop.razryad ? e.chop.razryad + ' разряд' : 'разряд не указан';
      const shift = e.chop.shift_mode || 'режим смен не указан';
      const weapon = e.chop.weapon_access ? ', допуск к оружию/спецсредствам' : '';
      blocks.push(bul(safe(e.full_name) + ' — пост «' + post + '», ' + razryad + ', режим: ' + shift + weapon + '.'));
    });
  }

  const ch = [
    ...orderHead(c, num, '«Об утверждении табеля постов охраны»'),
    ...eL(1),
    p('В соответствии с Законом РФ от 11.03.1992 № 2487-1 «О частной детективной и охранной деятельности в Российской Федерации»,', { indent: true }),
    p('ПРИКАЗЫВАЮ:', { bold: true }),
    p('1. Утвердить с ' + safe(c.doc_date) + ' табель постов охраны согласно приложению.'),
    SH('Приложение. Табель постов охраны'),
    ...blocks,
    p('2. Ответственным за соблюдение табеля постов и контроль несения службы назначить ' + declRole(safe(c.manager_position),'acc') + ' ' + safe(c.manager_name) + '.'),
    p('3. Табель постов пересматривать при изменении объектов охраны, состава постов или численности сотрудников.'),
    p('4. Контроль за исполнением оставляю за собой.'),
    ...orderSign(c),
  ];
  return save(
    [{ properties: { page: { size: { width: 11906, height: 16838 }, margin: MP } }, footers: { default: footer('chop.01') }, children: ch }],
    dir, 'Приказ об утверждении табеля постов охраны.docx'
  );
}

// Приказ о допуске к оружию и специальным средствам — только если хотя бы
// у одного сотрудника отмечен weapon_access. Ст. 11.1 Закона № 2487-1 требует
// отдельного учёта лиц, допущенных к оружию.
async function gen_chop_weapon_order(c, s, dir) {
  const employees = (c.employees || []).filter(e => e.chop && e.chop.weapon_access);
  if (!employees.length) return null; // нет допущенных — документ не нужен

  const num = oNum(c, 22);
  const list = employees.map((e, i) => {
    const razryad = e.chop.razryad ? e.chop.razryad + ' разряд' : 'разряд не указан';
    return p((i + 1) + '. ' + safe(e.full_name) + ' — ' + safe(e.position) + ', ' + razryad + '.', { indent: true });
  });

  const ch = [
    ...orderHead(c, num, '«О допуске работников к оружию и специальным средствам»'),
    ...eL(1),
    p('В соответствии со ст. 11.1, 12 Закона РФ от 11.03.1992 № 2487-1 «О частной детективной и охранной деятельности в Российской Федерации», при наличии действующего удостоверения частного охранника, разрешения органов внутренних дел и медицинских допусков,', { indent: true }),
    p('ПРИКАЗЫВАЮ:', { bold: true }),
    p('1. Допустить к получению и применению служебного оружия и специальных средств следующих работников:'),
    ...list,
    p('2. Ответственному за оружейную комнату (при наличии) выдавать оружие и спецсредства только лицам, указанным в п. 1, при предъявлении удостоверения частного охранника.'),
    p('3. Допуск действителен при условии наличия у работника действующих медицинских допусков (медицинская справка 002-ЧО/у, психиатрическое освидетельствование). При истечении срока действия допуска — приостановить выдачу оружия работнику до его продления.'),
    p('4. Контроль за исполнением оставляю за собой.'),
    ...orderSign(c),
  ];
  return save(
    [{ properties: { page: { size: { width: 11906, height: 16838 }, margin: MP } }, footers: { default: footer('chop.02') }, children: ch }],
    dir, 'Приказ о допуске к оружию и специальным средствам.docx'
  );
}

