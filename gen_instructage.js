// ── Определение отрасли по ОКВЭД ───────────────────────────────────────────
function getIndustry(okved) {
  if (!okved) return 'OFFICE';
  const code = okved.replace(/\D/g, '').padStart(2, '0').slice(0, 2);
  const n = parseInt(code, 10);
  if (n >= 1  && n <= 3)  return 'AGRICULTURE';
  if (n >= 41 && n <= 43) return 'CONSTRUCTION';
  if (n >= 45 && n <= 47) return 'TRADE';
  if (n === 49 || n === 52) return 'TRANSPORT';
  if (n === 56) return 'CATERING';
  if (n >= 62 && n <= 63) return 'OFFICE';
  if (n === 86) return 'MEDICINE';
  return 'OFFICE'; // дефолт — офис
}

